/**
 * SignalsmithStretch.js から AudioWorklet 用の静的モジュールを生成する。
 * 実行: node scripts/build-signalsmith-worklet.js
 */
const fs = require('fs');
const path = require('path');

const srcPath = path.join(__dirname, '../js/vendor/SignalsmithStretch.js');
const outPath = path.join(__dirname, '../js/vendor/signalsmith-stretch-worklet.js');
const code = fs.readFileSync(srcPath, 'utf8');

const regStart = code.indexOf('function registerWorkletProcessor');
const regProcLine = code.indexOf(
    'registerProcessor(audioNodeKey, WasmProcessor);',
    regStart,
);
const regEnd = code.indexOf('\n', regProcLine) + 2;
const registerSrc = code.slice(regStart, regEnd);

const factoryStart = code.indexOf('function(moduleArg');
const factoryEnd = code.indexOf('\n);\n', factoryStart);
if (factoryStart < 0 || factoryEnd < 0) {
    console.error('Could not locate wasm factory in SignalsmithStretch.js');
    process.exit(1);
}
const factorySrc = code.slice(factoryStart, factoryEnd);

const out =
    '/* Auto-generated by scripts/build-signalsmith-worklet.js — do not edit */\n' +
    '(function () {\n' +
    'const Module = ' +
    factorySrc +
    ';\n' +
    registerSrc +
    '\nregisterWorkletProcessor(Module, "signalsmith-stretch");\n' +
    '})();\n';

fs.writeFileSync(outPath, out);
console.log('Wrote', outPath, '(' + out.length + ' bytes)');

try {
    new Function(out);
    console.log('Syntax check: OK');
} catch (e) {
    console.error('Syntax check FAILED:', e.message);
    process.exit(1);
}
